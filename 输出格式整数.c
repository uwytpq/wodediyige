/* 文件名：int_formats.c
 * 功能：演示整数（%d）的格式化输出，包括宽度、精度、左对齐、补零、正号等。
 */

#include <stdio.h>

int main() {
    int num = 42;
    int neg = -42;

    printf("=== 整数格式化示例 ===\n");

    // 1. 基本输出
    printf("1. 默认：%d\n", num);
    
    // 2. 最小宽度（右对齐，默认右对齐）
    printf("2. 宽度5，右对齐：%5d\n", num);      // 输出 "   42"
    printf("3. 宽度5，左对齐（负号）：%-5d\n", num); // 输出 "42   "
    
    // 3. 用0填充
    printf("4. 宽度5，0填充：%05d\n", num);      // 输出 "00042"
    
    // 4. 精度（最少数字位数，不足补0，精度会覆盖宽度）
    printf("5. 精度3，宽度5：%5.3d\n", num);     // 输出 "  042"（先满足精度3位数字"042"，宽度5右对齐）
    printf("6. 精度3，宽度5，负数：%5.3d\n", neg);// 输出 " -042"（负号占一位）
    
    // 5. 始终显示正号
    printf("7. 显示正号：%+d\n", num);           // 输出 "+42"
    printf("8. 显示正号，宽度5：%+5d\n", num);   // 输出 "  +42"
    
    // 6. 无符号整数（%u）
    unsigned int u = 40000;
    printf("9. 无符号整数：%u\n", u);
    printf("10. 无符号宽度8，0填充：%08u\n", u); // 输出 "00040000"
    
    // 7. 十六进制大写与小写
    int hex = 255;
    printf("11. 十六进制小写：%x\n", hex);       // 输出 "ff"
    printf("12. 十六进制大写：%X\n", hex);       // 输出 "FF"
    printf("13. 带前缀的十六进制：%#x\n", hex);  // 输出 "0xff"
    
    // 8. 八进制
    printf("14. 八进制：%o\n", hex);             // 输出 "377"
    printf("15. 带前缀的八进制：%#o\n", hex);    // 输出 "0377"
    
    return 0;
}
