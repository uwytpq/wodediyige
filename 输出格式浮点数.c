/* 文件名：float_formats.c
 * 功能：演示浮点数（%f、%e、%g）的格式化输出，包括小数位数、宽度、补零、科学记数法等。
 */

#include <stdio.h>

int main() {
    double pi = 3.14159265358979;
    double large = 123456.789;
    double small = 0.000012345;

    printf("=== 浮点数格式化示例 ===\n");

    // 1. 基本输出
    printf("1. 默认6位小数：%f\n", pi);          // 输出 "3.141593"
    
    // 2. 控制小数位数（四舍五入）
    printf("2. 保留2位小数：%.2f\n", pi);        // 输出 "3.14"
    printf("3. 保留0位小数：%.0f\n", pi);        // 输出 "3"
    
    // 3. 宽度与对齐
    printf("4. 宽度10，右对齐：%10.2f\n", pi);   // 输出 "      3.14"
    printf("5. 宽度10，左对齐：%-10.2f\n", pi);  // 输出 "3.14      "
    printf("6. 宽度10，0填充：%010.2f\n", pi);   // 输出 "0000003.14"
    
    // 4. 科学记数法
    printf("7. 科学记数法（默认）：%e\n", large); // 输出 "1.234568e+05"
    printf("8. 科学记数法，小数2位：%.2e\n", large);
    printf("9. 科学记数法，宽度12：%12.2e\n", large);
    
    // 5. 自动选择格式（%g）
    printf("10. %%g 自动选择：%g\n", large);     // large 用科学记数法显示
    printf("11. %%g 自动选择：%g\n", pi);        // pi 用小数显示
    printf("12. %%g 指定精度（有效数字）：%.3g\n", pi); // 输出 "3.14"
    
    // 6. 小数的科学记数法
    printf("13. 极小数的科学记数法：%e\n", small); // 输出 "1.234500e-05"
    printf("14. 极小数的自动格式：%g\n", small);
    
    // 7. 始终显示小数点
    printf("15. 即使整数也显示小数点：%#.0f\n", pi); // 输出 "3."
    
    return 0;
}
